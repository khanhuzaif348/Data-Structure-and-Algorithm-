# lets Start with Variables in Python
# Variables are used to store data in Python
# A variable is created when you assign a value to it
# Example of variable declaration and assignment
age = 25               # Integer variable
# Example of variable declaration and assigning values

x = 5           # Integer variable
y = 3.14        # Float variable    
name = "Alice"  # String variable
is_active = True  # Boolean variable
# You can print the value of a variable using the print() function
print(x)
print(y)    
print(name)
print(is_active)

# Naming Conventions
# Variable names should start with a letter or an underscore (_)
# They can contain letters, numbers, and underscores
# Variable names are case-sensitive
# Examples of valid variable names
my_variable = 10
_variable2 = 20
variable_name = "Hello"
VariableName = "World"  # Different from variable_name
# Examples of invalid variable names (uncommenting these will cause errors)
# 2variable = 30      # Starts with a number
# my-variable = 40    # Contains a hyphen
# my variable = 50    # Contains a space
# Understanding Variable Types
# Python has several built-in data types
# Common data types include:
# Integer (int): Whole numbers, e.g., 1, -5, 100
# Float (float): Decimal numbers, e.g., 3.14, -0.001
# String (str): Text, e.g., "Hello", 'Python'
# Boolean (bool): True or False values
# Type Checking and Conversion
# You can check the type of a variable using the type() function
print(type(x))          # Output: <class 'int'>
print(type(y))          # Output: <class 'float'>
print(type(name))       # Output: <class 'str'>

